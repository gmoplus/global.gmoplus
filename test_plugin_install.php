<?php
// Plugin install test
require_once 'includes/config.inc.php';

try {
    // Plugin dosyasını dahil et
    require_once 'plugins/quoteRequest/rlQuoteRequest.class.php';
    
    // Plugin instance oluştur
    $quoteRequest = new rlQuoteRequest();
    
    // Install metodunu çağır
    $result = $quoteRequest->install();
    
    if ($result) {
        echo "✅ Plugin başarıyla kuruldu!<br>";
        echo "✅ fl_quote_requests tablosu oluşturuldu!<br>";
        echo "✅ Upload klasörü oluşturuldu!<br>";
    } else {
        echo "❌ Plugin kurulumu başarısız!<br>";
    }
    
} catch (Exception $e) {
    echo "❌ Hata: " . $e->getMessage() . "<br>";
}

// Tabloyu kontrol et
try {
    $mysqli = new mysqli(RL_DBHOST, RL_DBUSER, RL_DBPASS, RL_DBNAME, RL_DBPORT);
    
    $result = $mysqli->query("SHOW TABLES LIKE 'fl_quote_requests'");
    
    if ($result->num_rows > 0) {
        echo "✅ Tablo mevcut!<br>";
        
        // Tablo yapısını göster
        $result = $mysqli->query("DESCRIBE fl_quote_requests");
        echo "<h3>Tablo Yapısı:</h3>";
        while ($row = $result->fetch_assoc()) {
            echo $row['Field'] . " - " . $row['Type'] . "<br>";
        }
        
    } else {
        echo "❌ Tablo bulunamadı!<br>";
    }
    
    $mysqli->close();
    
} catch (Exception $e) {
    echo "❌ Database kontrol hatası: " . $e->getMessage() . "<br>";
}
?> 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 